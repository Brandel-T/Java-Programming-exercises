
public class Test1 {
	public static void main(String[] args) { 
		System.out.println( 17 | 4 ); //ODER-GATTER
		System.out.println( 17 ^ 4 ); //XOR-GATTER
		System.out.println( 17 & 4 ); //AND-GATTER
		
		System.out.println( 17 >> 4 );
		System.out.println( 17 << 4 );
		System.out.println( 17 >>> 3 );
	}
}
