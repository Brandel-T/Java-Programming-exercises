package GenericContainer;

public class WortPaar
{
	private String deWord;
	private String enWord;
	
	public WortPaar(String de, String en) {
		deWord = de;
		enWord = en;
	}

	public String getDeWord() {
		return deWord;
	}

	public void setDeWord(String deWord) {
		this.deWord = deWord;
	}

	public String getEnWord() {
		return enWord;
	}

	public void setEnWord(String enWord) {
		this.enWord = enWord;
	}
	
	
	
	
}
